class Vert2Joint:
    def __init__(self, vert2joint_path="asset/ours/vert2joint.pkl"):
        with open(vert2joint_path, 'rb') as f:
            vert2joint = pickle.load(f)
        self.vert2joint = vert2joint['vert2joint']
        self.vert2joint_pth = torch.from_numpy(self.vert2joint).float()

    def __call__(self, verts):
        """
        Args:
            verts: [..., 778, 3]
        Returns:
            joints: [..., 21, 3]
        """
        if isinstance(verts, torch.Tensor):
            self.vert2joint_pth = self.vert2joint_pth.to(verts.device)
            joints = torch.einsum("...ij,ki->...kj", verts, self.vert2joint_pth)
        elif isinstance(verts, np.ndarray):
            joints = np.einsum("...ij,ki->...kj", verts, self.vert2joint)
        else:
            raise NotImplementedError
        return joints
