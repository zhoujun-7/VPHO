def get_joint_aligned_with_HO3D(vert, joint):
    """ used to get joints that aligned with the joint annotation of HO3D dataset."""
    joint_ho3d_order = joint_reorder(joint, 'manolayer')
    tips_id = [16, 17, 18, 19, 20]
    tips_vert_id = [728, 353, 442, 576, 694]
    new_tips = vert[..., tips_vert_id, :]
    joint_ho3d_order[..., tips_id, :] = joint_ho3d_order[..., tips_id, :]*0 + new_tips
    return joint_ho3d_order


def get_mean_joint_error(pd, gt):
    """
    Args:
        pd: [..., 21, 3]
        gt: [..., 21, 3]
    Returns:
        error: [...]
    """
    error = np.sqrt(np.sum((pd - gt)**2, axis=-1))
    return error


def rigid_transform_3D(A, B):
    n, dim = A.shape
    centroid_A = np.mean(A, axis = 0)
    centroid_B = np.mean(B, axis = 0)
    H = np.dot(np.transpose(A - centroid_A), B - centroid_B) / n
    U, s, V = np.linalg.svd(H)
    R = np.dot(np.transpose(V), np.transpose(U))
    if np.linalg.det(R) < 0:
        s[-1] = -s[-1]
        V[2] = -V[2]
        R = np.dot(np.transpose(V), np.transpose(U))

    varP = np.var(A, axis=0).sum()
    c = 1/varP * np.sum(s) 

    t = -np.dot(c*R, np.transpose(centroid_A)) + np.transpose(centroid_B)
    return c, R, t


def rigid_align(A,B):
    c, R, t = rigid_transform_3D(A, B)
    A2 = np.transpose(np.dot(c*R, np.transpose(A))) + t
    return A2


def get_pa_mean_joint_error(pd, gt):
    """
    Args:
        pd: [..., 21, 3]
        gt: [..., 21, 3]
    Returns:
        error: [...]
    """
    joints_out_aligned = []
    for i in range(pd.shape[0]):
        joints_out_aligned_i = rigid_align(pd[i], gt[i])
        joints_out_aligned.append(joints_out_aligned_i)

    joints_out_aligned = np.stack(joints_out_aligned, axis=0)
    error = np.sqrt(np.sum((joints_out_aligned - gt)**2, axis=-1))
    return error


def get_force_point_and_direction_from_mano_vert(vert):
    # vert: [N, 778, 3]

    point = vert[..., FORCE_FACE[:, 0], :] + vert[..., FORCE_FACE[:, 1], :] + vert[..., FORCE_FACE[:, 2], :]
    point = point / 3

    vecz_1 = vert[..., FORCE_FACE[:, 1], :] - vert[..., FORCE_FACE[:, 0], :]
    vecz_2 = vert[..., FORCE_FACE[:, 2], :] - vert[..., FORCE_FACE[:, 1], :]

    joint = VERT2JOINT(vert)
    skeleton = np.concatenate([SKELETON_LEVEL[0][[1, 2, 3, 4, 4]], SKELETON_LEVEL[1], SKELETON_LEVEL[2], SKELETON_LEVEL[3]])
    vecy = joint[..., skeleton[:, 1], :] - joint[..., skeleton[:, 0], :]

    if isinstance(vert, torch.Tensor):
        direction_z = torch.cross(vecz_1, vecz_2, dim=-1)
        direction_z = direction_z / (torch.norm(direction_z, dim=-1, keepdim=True)+1e-8)

        vecy = vecy / (torch.norm(vecy, dim=-1, keepdim=True)+1e-8)
        direction_x = torch.cross(vecy, direction_z, dim=-1)
        direction_x = direction_x / (torch.norm(direction_x, dim=-1, keepdim=True)+1e-8)

        direction_y = torch.cross(direction_z, direction_x, dim=-1)
        direction_y = direction_y / (torch.norm(direction_y, dim=-1, keepdim=True)+1e-8)

        frame = torch.stack([direction_x, direction_y, direction_z], dim=-1)
    elif isinstance(vert, np.ndarray):
        direction_z = np.cross(vecz_1, vecz_2, axis=-1)
        direction_z = direction_z / (np.linalg.norm(direction_z, axis=-1, keepdims=True)+1e-8)
    
        vecy = vecy / (np.linalg.norm(vecy, axis=-1, keepdims=True)+1e-8)
        direction_x = np.cross(vecy, direction_z, axis=-1)
        direction_x = direction_x / (np.linalg.norm(direction_x, axis=-1, keepdims=True)+1e-8)

        direction_y = np.cross(direction_z, direction_x, axis=-1)
        direction_y = direction_y / (np.linalg.norm(direction_y, axis=-1, keepdims=True)+1e-8)

        frame = np.stack([direction_x, direction_y, direction_z], axis=-1)
    else:
        raise NotImplementedError

    return point, frame


